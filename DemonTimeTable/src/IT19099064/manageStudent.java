    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
      
          ID.setText( studentTable.getValueAt(studentTable.getSelectedRow(), 0).toString());
        gid1.setText( studentTable.getValueAt(studentTable.getSelectedRow(), 5).toString());
        sgid1.setText( studentTable.getValueAt(studentTable.getSelectedRow(), 6).toString());
        gn1.setText( studentTable.getValueAt(studentTable.getSelectedRow(), 3).toString());
        sgn1.setText( studentTable.getValueAt(studentTable.getSelectedRow(), 4).toString());
        ays1.setText( studentTable.getValueAt(studentTable.getSelectedRow(), 1).toString());
       
        
       String comboVal=studentTable.getValueAt(studentTable.getSelectedRow(), 2).toString();
        if(comboVal.equalsIgnoreCase("it")){
            prg1.setSelectedIndex(1);
        } else if (comboVal.equalsIgnoreCase("cs")) {
            prg1.setSelectedIndex(2);
        } else if (comboVal.equalsIgnoreCase("ndm")) {
            prg1.setSelectedIndex(3);
        } else if (comboVal.equalsIgnoreCase("itp")) {
            prg1.setSelectedIndex(4);
        } else {
            prg1.setSelectedIndex(0);
        }
        
         
    }//GEN-LAST:event_studentTableMouseClicked

