    private void setStudentTableData(){
    try{
        Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/ltdmgdb?zeroDateTimeBehavior=convertToNull","root","");
        int rows=0;
        int rowIndex=0;
         String sql = "select * from student order by id desc";
        PreparedStatement smt = c.prepareStatement(sql);
        ResultSet rs = smt.executeQuery(sql);

       
       if(rs.next())
       {
           rs.last();
           rows=rs.getRow();
           rs.beforeFirst();
       }
//       System.out.println(rows);

String[][] data= new String[rows][7];
while(rs.next())
{
   data[rowIndex][0]=rs.getInt(1)+"";
   data[rowIndex][1]=rs.getString(2);
    data[rowIndex][2]=rs.getString(3);
     data[rowIndex][3]=rs.getString(4);
      data[rowIndex][4]=rs.getString(5);
      data[rowIndex][5]=rs.getString(6);
       data[rowIndex][6]=rs.getString(7);
               
    
  
   rowIndex++;
       
    }

String[] cols={"ID","SEMESTER","PROGRAMME","GROUP NO","SUB GROUP NO","GROUP ID","SUB GROUP ID"};
DefaultTableModel model=new DefaultTableModel(data,cols);

studentTable.setModel(model);

rs.close();
smt.close();
    }catch(Exception ex){
        JOptionPane.showMessageDialog(null,"Cannot retrieve data !!");
    }
 
}

    

